def movies_by_category(m, c):
    return [m for m in movies if m.get('category') == c]

def average_imdb_score(m):
    if not m:
        return 0
    scores = [movie.get('imdb', 0) for movie in m]
    avg_score = round(sum(scores) / len(scores), 2)
    return avg_score

def average_imdb_score_by_category(m, c):
    category_movies = movies_by_category(m, c)
    return average_imdb_score(category_movies)

category = 'Romance'
